season = input('What season is going on? ')

if(season=='summer'):
    print("You should wear light weight summer clothes such as cotton T-shirts, shorts, light pants, singlet.")

elif(season=='winter'):
    print("You should wear heavy warm cloths such as scarf, jackets, hoodies, sweaters.")

elif(season=='monsoon'or'rainy'or'rain'):
    print("You should wear light weight clothes but not too light that the cold environment can harm you.")
    
else:
    print("You can wear clothes not too warm and not too cold")